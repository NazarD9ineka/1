# Початкові значення
a = 5
b = 10

print(f"До обміну: a = {a}, b = {b}")

# Міняємо місцями за допомогою третьої змінної (temp)
temp = a  # Крок 1: Копіюємо значення 'a' у тимчасове сховище
a = b     # Крок 2: Записуємо значення 'b' у змінну 'a'
b = temp  # Крок 3: Записуємо значення з 'temp' у змінну 'b'

print(f"Після обміну: a = {a}, b = {b}")
