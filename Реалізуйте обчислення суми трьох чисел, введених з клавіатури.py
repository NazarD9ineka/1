# Запитуємо три числа у користувача
number1 = float(input("Введіть перше число: "))
number2 = float(input("Введіть друге число: "))
number3 = float(input("Введіть третє число: "))

# Обчислюємо суму
total_sum = number1 + number2 + number3

# Виводимо результат
print(f"Сума введених чисел: {total_sum}")
